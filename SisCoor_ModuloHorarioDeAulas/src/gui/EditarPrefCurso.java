    private void jComboNPeriodoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboNPeriodoActionPerformed
        int num=Integer.parseInt(String.valueOf(jComboNPeriodo.getSelectedItem()));
        
        jComboPeriodo.removeAllItems();
        for(int i=1;i<=num;i++)
        {
            jComboPeriodo.addItem(String.valueOf(i));
        }
    }//GEN-LAST:event_jComboNPeriodoActionPerformed

    private void jBSalvarPeriodoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBSalvarPeriodoActionPerformed
        ArrayList<Periodo> aPer = new ArrayList();
        
        if(jTDisciplinasVinc.getRowCount()!=0){
            for(int i = 0; i<jTDisciplinasVinc.getRowCount();i++){
                aPer.add(new Periodo(Integer.parseInt(String.valueOf(jComboPeriodo.getSelectedItem())),
                        disciplinasDAO.buscar(new Disciplinas(String.valueOf(jTDisciplinasVinc.getValueAt(i, 0)),
                                Integer.parseInt(String.valueOf(jTDisciplinasVinc.getValueAt(i, 1)))))));
                
                perDao.inserir(aPer.get(i));
            }
        }
    }//GEN-LAST:event_jBSalvarPeriodoActionPerformed

