    private void jSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jSalvarActionPerformed
        
        Curso curso = new Curso();
        Professor professor = new Professor();
        ProfessorDAO professorDAO = new ProfessorDAO();
        boolean existente;
        
        if(jTFnomeCurso.getText().equals(""))
        {
            JOptionPane.showMessageDialog(rootPane, "Nome do curso invalido!", "Erro", JOptionPane.WARNING_MESSAGE);
        }
        
        else
        {
            curso.setNome(jTFnomeCurso.getText());
            curso.setTurno((String) jComboTurno.getSelectedItem());
            professor.setCod(professorDAO.buscar_Ativo());
            existente=cursoDAO.buscar_Repeticao(curso);
            
            if(existente==false)
            {
                curso.setProfCoord(professor.getCod());
                cursoDAO.inserir(curso);
                this.curso.setCod(cursoDAO.buscarCodCurso(curso));
            }
            
            else
            {
                JOptionPane.showMessageDialog(rootPane, "Curso existente", "Erro", JOptionPane.INFORMATION_MESSAGE);
            } 
        }
    }//GEN-LAST:event_jSalvarActionPerformed

    private void jBaddDispCursoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBaddDispCursoActionPerformed
        
        DefaultTableModel modelo = new DefaultTableModel(); 
        modelo=(DefaultTableModel) jTDisciplinasVinc.getModel();
        
        Disciplinas disciplina = new Disciplinas();
        
        disciplina.setNome((String) jTDisciplinasDispo.getValueAt(jTDisciplinasDispo.getSelectedRow(), 0));
        disciplina.setCargaHoraria((int) jTDisciplinasDispo.getValueAt(jTDisciplinasDispo.getSelectedRow(), 1));
       
        modelo.addRow(new Object[]{disciplina.getNome(),disciplina.getCargaHoraria()});
      
    }//GEN-LAST:event_jBaddDispCursoActionPerformed

